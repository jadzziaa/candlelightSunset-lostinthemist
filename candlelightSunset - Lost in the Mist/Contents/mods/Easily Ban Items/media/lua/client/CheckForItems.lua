local function bannedItemsCheck1()
    local playerObj = getPlayer()
    local BanItems1 = getSandboxOptions():getOptionByName("BanItems1"):getValue()
    if playerObj:getInventory():contains(BanItems1) then
        while playerObj:getInventory():contains(BanItems1) do
            playerObj:getInventory():Remove(BanItems1)
        end
    end
end



